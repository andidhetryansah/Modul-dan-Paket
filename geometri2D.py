import math
def luasPersegiPanjang(p, l):
   return p * l

def KelilingPersegiPanjang(p, l):
   return 2 * (p+l)

def luasBujursangkar(s):
   return s * s

def KelilingBujursangkar(s):
   return 4 * s

def luasLingkaran(r):
   return math.pi * r * r

def kelilingLingkaran(r):
   return 2 * math.pi * r

def luassegitiga (a, t):
   return (a * t) / 2
   
def kelilingsegitiga(a, b, c):
   return a + b + c
